/*
    Operadores Aritméticos
        
*/

// * multiplicação *
    console.log(5.6 * 23) 

// * divisão /
    console.log(24 / 2)

// * soma +
    console.log(54 + 83)

// * subtração -
    console.log(34 - 45)

// * resto da divisão %
    let remainder
    remainder = 23 % 5 
    console.log(remainder)

// * incremento ++
    let increment = 0
    increment++
    increment++
    console.log(increment)

// * decremento --
    let decrement = 0
    decrement--
    decrement--
    console.log(decrement)

// * exponencial **
    console.log(9 ** 3)